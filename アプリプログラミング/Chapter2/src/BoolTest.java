public class BoolTest {
    public static void main(String[] args) {
        boolean a = true, b = false;

        System.out.println("a || b = " + (a || b));     //論理和
        System.out.println("a && b = " + (a && b));     //論理積
        System.out.println("!a = " + !a);               //否定
    }
}

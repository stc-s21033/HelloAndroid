/**
 * 四則演算＋剰余演算
 */
public class SimpleCalc {
    public static void main(String[] args) {
        System.out.println("1+2 = " + (1+2));
        System.out.println("5-3 = " + (5-3));
        System.out.println("6x2 = " + (6*2));
        System.out.println("11/3 = " + (11.0/3.0));
        System.out.println("7%2 = " + (7%2));
    }
}
